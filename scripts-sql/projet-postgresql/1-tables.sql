CREATE TABLE benevole(
	idpersonne 		INT NOT NULL,
	type 			VARCHAR(10),
	PRIMARY KEY (idpersonne),
	FOREIGN KEY (idpersonne) REFERENCES personne(idpersonne)
)WITHOUT OIDS;


------------------------------------------------------------
-- Table: coureur
------------------------------------------------------------
CREATE TABLE coureur(
	idpersonne 		INT NOT NULL,
	poste 			VARCHAR(10),
	club			VARCHAR(30),
	PRIMARY KEY (idpersonne),
	FOREIGN KEY (idpersonne) REFERENCES personne(idpersonne)
)WITHOUT OIDS;

------------------------------------------------------------
-- Table: poste
------------------------------------------------------------
CREATE TABLE poste(
	idposte 		INT NOT NULL,
	idbenevole		INT NOT NULL,
	libelle 		VARCHAR(100),
	nombreplace		INT,
	heured 		VARCHAR(100),
	heuref 		VARCHAR(100),
	PRIMARY KEY (idposte),
	FOREIGN KEY (idbenevole) REFERENCES benevole (idpersonne)
)WITHOUT OIDS;



-- Vues

CREATE VIEW v_compte_avec_roles AS
	SELECT c.*, ARRAY_AGG( r.role ) AS roles
	FROM compte c 
	LEFT JOIN ROLE r ON c.idcompte = r.idcompte
	GROUP BY c.idcompte;

